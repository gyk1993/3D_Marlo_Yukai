function msg = EtherCATDCError(code)
    codes = {...
        0,  'EtherCAT DC working normally (no error)'; ...
        1,  'Init function not called or not successful'; ...
        2,  'Controller error — synchronization out of limit'; ...
        3,  'Not enough memory'; ...
        4,  'Hardware layer — (BSP) invalid'; ...
        5,  'Hardware layer — error modifying the timer'; ...
        6,  'Hardware layer — timer is not running'; ...
        7,  'Hardware layer — function is called on wrong CPU'; ...
        8,  'Invalid DC sync period length'; ...
        9,  'Error DCM Controller SetVal is too small'; ...
        10, 'Error DCM Controller — Drift between local timer and ref clock too high'; ...
        };
    
    if isstruct(code)
        code = code.EtherCATStatus(end,3);
    end
    
    idx = find([codes{:,1}] == code);
    if ~isempty(idx)
        msg = codes{idx,2};
    else
        msg = 'Unknown EtherCAT DC Error code.';
    end
end
