function ModbusPacket = createModbusPacket32(ModbusAddress, ModbusFunction, ModbusData) %#codegen
    ModbusPacket = [...
        uint32(ModbusAddress); ...
        uint32(ModbusFunction); ...
        uint32(ModbusData); ...
        zeros(2,1, 'uint32'); ...
        ];
    
    ModbusPacket(end-1:end) = uint32(computeModbusCRC(ModbusPacket(1:end-2)));
end
